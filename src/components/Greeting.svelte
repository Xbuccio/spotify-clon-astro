<script lang="ts">
  const currenTime = new Date()
  const currenHour = currenTime.getHours()

  let greeting = ""

  if (currenHour > 12) {
    greeting = "Buenos dias"
  } else if (currenHour < 18) {
    greeting = "Buenas tarde"
  } else {
    greeting = "Buenas noches"
  }
</script>

<h1 class="text-3xl font-bold">{greeting}</h1>